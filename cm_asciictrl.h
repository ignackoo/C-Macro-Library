#ifndef __CM_ASCIICTRL_H
#define __CM_ASCIICTRL_H


/* control characters */
#define CTRL_NUL                 CHAR_NUL /* NUL Null */
#define CTRL_SOH                 CHAR_SOH /* SOH Start of heading */
#define CTRL_STX                 CHAR_STX /* STX Start of text */
#define CTRL_ETX                 CHAR_ETX /* ETX End of text */
#define CTRL_EOT                 CHAR_EOT /* EOT End of transmission */
#define CTRL_ENQ                 CHAR_ENQ /* ENQ Enquiry */
#define CTRL_ACK                 CHAR_ACK /* ACK Acknowledge */
#define CTRL_BEL                 CHAR_BEL /* BEL Bell */
#define CTRL_BS                  CHAR_BS  /* BS  Backspace */
#define CTRL_HT                  CHAR_HT  /* HT  Horizontal tab */
#define CTRL_LF                  CHAR_LF  /* LF  Line feed */
#define CTRL_VT                  CHAR_VT  /* VT  Vertical tab */
#define CTRL_FF                  CHAR_FF  /* FF  New page / form feed */
#define CTRL_CR                  CHAR_CR  /* CR  Carriage return */
#define CTRL_SO                  CHAR_SO  /* SO  Shift out */
#define CTRL_SI                  CHAR_SI  /* SI  Shift in */
#define CTRL_DLE                 CHAR_DLE /* DLE Data link escape */
#define CTRL_DC1                 CHAR_DC1 /* DC1 Device control 1 */
#define CTRL_DC2                 CHAR_DC2 /* DC2 Device control 2 */
#define CTRL_DC3                 CHAR_DC3 /* DC2 Device control 3 */
#define CTRL_DC4                 CHAR_DC4 /* DC4 Device control 4 */
#define CTRL_NAK                 CHAR_NAK /* NAK Negative acknowledge */
#define CTRL_SYN                 CHAR_SYN /* SYN Synchronous idle */
#define CTRL_ETB                 CHAR_ETB /* ETB End of transmission block */
#define CTRL_CAN                 CHAR_CAN /* CAN Cancel */
#define CTRL_EM                  CHAR_EM  /* EM  End of medium */
#define CTRL_SUB                 CHAR_SUB /* SUB Substitute */
#define CTRL_ESC                 CHAR_ESC /* ESC Escape */
#define CTRL_FS                  CHAR_FS  /* FS  File separator */
#define CTRL_GS                  CHAR_GS  /* GS  Group separator */
#define CTRL_RS                  CHAR_RS  /* RS  Record separator */
#define CTRL_US                  CHAR_US  /* US  Unit separator */

#define CTRL_DEL                 CHAR_DEL /* DEL Delete */


#endif /* __CM_ASCIICTRL_H */
